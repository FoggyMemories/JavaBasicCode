public class MethodDemo6_1 {
    public static void main(String[] args) {
        /*
        方法的重载
            在同一个类中,定义了多个同名的方法,这些同名的方法具有同种功能.
            每个方法具有不同的参数类型或者参数个数,这些同名的方法,就构成了重载关系.

            简单记:同一个类中,方法名相同,参数不同的方法.与返回值无关.
                   参数不同:个数不同、类型不同、顺序不同
            java虚拟机 会通过 参数 的不同来区分同名的方法    *实参和形参要一一对应*
         */
    }
}
