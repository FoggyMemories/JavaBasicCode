package com.test.arithmeticoperator;

public class ArithmeticoperatorDemo4 {
    public static void main(String[] args) {
        //++和--
        int a = 10;
        a++;
        System.out.println(a);  //先用后加

        ++a;
        System.out.println(a);  //先加后用

        a--;
        System.out.println(a);

        --a;
        System.out.println(a);




    }
}
